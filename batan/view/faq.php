      <A NAME=Apasih_BATAN_itu style="margin-left:15px;">
      <b>Apasih BATAN itu?</b></a><br>
      <blockquote style="margin-left:15px;">BATAN adalah lembaga pemerintah non departemen yang melakukan penilitian dan
      pengembangan. BATAN berada di bawah Kementrian Negara Riset dan Teknologi, seperti juga BPPT,
      LIPI, BAPETEN, BSN, BAKOSURTANAL, LAPAN, Lembaga Eikjman.</blockquote>
      </font><br>

      <A NAME=Apa_yang_sudah_dikembangkan_oleh_BATAN style="margin-left:15px;">
      <b>Apa yang sudah dikembangkan oleh BATAN?</b></a><br>
      <blockquote style="margin-left:15px;">BATAN mengembangkan teknik nuklir untuk bidang pangan, energi, kesehatan & obat,
      diseminasi & teknologi informasi bidang nuklir. </blockquote>
      </font><br>

      <A NAME=Apakah_BATAN_punya_cabang_di_daerah style="margin-left:15px;">
      <b>Apakah BATAN punya cabang di daerah?</b></a><br>
      <blockquote style="margin-left:15px;">BATAN tidak memiliki kantor cabang, namun BATAN memiliki fasilitas penelitian di
      berbagai kota. BATAN memiliki fasilitas di Pasar Jum'at (dekat Terminal Lebak Bulus), Jakarta Selatan,
      di PUSPIPTEK Serpong Tangerang, di Bandung (dekat ITB), di Jl. Babarsari, Sleman, Yogyakarta,
      dan fasilitas pemantauan meteorologi di Ujungwatu, Jepara.</blockquote>
      </font><br>

      <A NAME=Apakah_BATAN_sama_dengan_BAPETEN style="margin-left:15px;">
      <b>Apakah BATAN sama dengan BAPETEN?</b></a><br>
      <blockquote style="margin-left:15px;">Berdasarkan UU No.10 Tahun 1997 tentang Ketenaganukliran, BATAN memiliki tugas
      penelitian dan pengembangan, penyelidikan umum, eksplorasi dan eksploitasi bahan galian nuklir,
      produksi bahan baku untuk pembuatan dan produksi bahan bakar nuklir, produksi radioisotop untuk
      keperluan penelitian dan pengembangan, dan pengelolaan limbah radioaktif. Sementara BAPETEN memiliki
      tugas pengaturan, perizinan, dan inspeksi. Dengan kata lain, BATAN yang melakukan litbang, BAPETEN
      yang melakukan pengawasan. Reaktor Nuklir BATAN selalu diinspeksi oleh BAPETEN, dan tentu saja
      oleh IAEA.</blockquote>
      </font><br>

      <A NAME=Apakah_hubungan_antara_BATAN_dan_PLTN style="margin-left:15px;">
      <b>Apakah ada hubungan antara BATAN dan PLTN?</b></a><br>
      <blockquote style="margin-left:15px;">BATAN, bersama-sama dengan BPPT, Dept. ESDM, PLN dan instansi terkait lainnya serta
      dengan bantuan IAEA pada tahun 2001-2002, melakukan kajian dan perhitungan tentang layak-tidaknya
      PLTN dibangun dan dioperasikan di Indonesia pada sekitar tahun 2017.</blockquote>
      </font><br>
      <A NAME=Apakah_BATAN_mengembangkan_senjata_nuklir style="margin-left:15px;">
      <b>Apakah BATAN mengembangkan senjata nuklir?</b></a><br>
      <blockquote style="margin-left:15px;">BATAN hanya mengembangkan dan meneliti nuklir untuk maksud-maksud damai, tidak untuk
      keperluan merusak, persenjataan ataupun kepentingan militer lainnya. BATAN menjadi salah satu
      penandatangan traktat NPT (<i>Nuclear Non Proliferation Treaty</i>) atau pakta untuk tidak
      menyebarluaskan senjata nuklir. Traktat itu sendiri mulai dirintis sejak 1 Juli 1968. Dari 180
      lebih anggota Badan Tenaga Nuklir Internasional (IAEA = <i>International Atomic Energy Agency</i>),
      hanya 4 negara yang tidak berpartisipasi dalam traktat ini. Negara tersebut adalah India, Israel,
      Korea Utara dan Pakiskan. BATAN juga tidak melakukan upaya-upaya pengkayaan uranium.</blockquote>
      </font><br>

      <A NAME=Kenapa_kita_butuh_PLTN style="margin-left:15px;">
      <b>Kenapa kita butuh PLTN?</b></a><br>
      <blockquote style="margin-left:15px;">Sesuai Peraturan Presiden RI No. 5 Tahun 2006 tentang Kebijaksanaan Energi Nasional
      telah menetapkan sasaran terwujudnya bauran energi (<i>energy mix</i>) yang optimal pada tahun
      2O25, yaitu peranan masing-masing jenis energi terhadap konsumsi energi nasional. Sasaran yang
      ditargetkan khususnya untuk biomassa, nuklir, tenaga air, tenaga surya dan tenaga angin menjadi
      lebih dari 5%. Sesuai dengan Undang-undang No. 17 tahun 2007 tentang RPJPN (Rencana Pembangunan
      Jangka Panjang Nasional), pada RPJM (Rencana Pembangunan Jangka Menengah) ke-3 (2015-2019) mulai
      dimanfaatkannya tenaga nuklir untuk pembangkitan listrik dengan mempertimbangkan
      faktor keselamatan yang ketat dan sesuai dengan Undang-undang No. 30 tahun 2007 tentang Energi,
      bahwa sumber daya energi fosil, panas bumi, hidro skala besar dan sumber energi nuklir dikuasai
      oleh negara dan dimanfaatkan untuk sebesar-besarnya untuk kemakmuran rakyat.</blockquote>
