              <p class="area_text">Sistem radiografi real-time untuk inspeksi produk industri manufaktur diperlukan dengan inspeksi roda (velg) secara tidak merusak (nondestructive evaluation). Generator Sinar-X beroperasi secara kontinyu pada tegangan tinggi 74 KV, 3 mA dan mampu menembus ketebalan hingga 18mm.</p>
              <p class="area_text">Sistem radiografi tersebut menunjang proses inspeksi produk secara tak merusak untuk mendukung gugus kendali mutu manufaktur. Unjuk kerja sistem resolusinya dari 300 \u2013 800 lines/inchi dan secara visual mampu membedakan sejumlah cacat internal pada produk roda alluminium (Velg-Al). Sistem Radiografi tersebut sudah dipergunakan oleh PT. Pakoakuina.</p>
