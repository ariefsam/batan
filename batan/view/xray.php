			<p class="area_text">Pesawat sinar-X tipe XR-2302 dirancang khusus untuk keperluan radiografi diagnostik dengan keutamaan jaminan keselamatan bagi pasien maupun operator. Sistem proteksi radiasi dirancang sesuai dengan pedoman yang dikeluarkan oleh ILO, IAEA, WHO, BATAN dan DEPKES RI, meliputi pengoperasian, pembatasan berkas radiasi, alat pengaman, pengaturan dan pembatasan waktu penyinaran.</p>
