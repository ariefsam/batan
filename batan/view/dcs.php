			<p class="text_subjudul">DISTRIBUTED CONTROL SYSTEM</p>

                <p class="area_text">Kontrol proses dengan menggunakan DCS (<i>Distributed Control System</i>) dan NCS (<i>Nucleonic Control System</i>) di industri kertas yang lazim disebut <i>Basis Weight</i>, <i>Moisture and Ash Control System</i>. Sistem ini terpasang secara "<i>on-line</i>" tanpa mengganggu proses produksi, dari tampilan layar monitor tergambar grafik dan profil aktual pengukuran.</p>

                <p class="area_text">Bila penyimpangan terjadi, maka DCS/NCS secara otomatis mengadakan pengendalian ke persediaan bahan baku dengan membuka dan menutup "<i>valve</i>" secara proporsional.</p>
