                <p class="text_subjudul">APPRON TIMBAL</p>
                <p class="area_text">Prinsip proteksi radiasi bahwa dosis radiasi sekecil apapun harus dihindari. Karena efek stokastik (genetik) mempunyai peluang timbul akibat perubahan sel normal walaupun pada dosis rendah dan tidak mengenal dosis ambang. Maka operator, pembantu operator, lingkungan dan bagian vital pasien harus dilindungi dari radiasi yang tidak perlu. Untuk menghindari resiko terebut maka instalasi medik/rumah sakit yang memakai pesawat Sinar-X/sumber radiasi harus menyediakan peralatan pelindung.</p>
