		Badan Tenaga Nuklir Nasional Liris Open Source IGOS</div>

		<p><center><img src="" border="0" name="" alt="" width=80%></center></p>

                <p class="area_text">Kegiatan pengembangan dan pengaplikasian teknologi nuklir di Indonesia diawali dari pembentukan Panitia Negara untuk Penyelidikan Radioaktivitet tahun 1954. Panitia Negara tersebut mempunyai tugas melakukan penyelidikan terhadap kemungkinan adanya jatuhan radioaktif dari uji coba senjata nuklir di lautan Pasifik.</p>
				<p class="area_text">Dengan memperhatikan perkembangan pendayagunaan dan pemanfaatan tenaga atom bagi kesejahteraan masyarakat, maka melalui Peraturan Pemerintah No. 65 tahun 1958, pada tanggal 5 Desember 1958 dibentuklah Dewan Tenaga Atom dan Lembaga Tenaga Atom (LTA), yang kemudian disempurnakan menjadi Badan Tenaga Atom Nasional (BATAN) berdasarkan UU No. 31 tahun 1964 tentang Ketentuan-ketentuan Pokok Tenaga Atom. Selanjutnya setiap tanggal 5 Desember yang merupakan tanggal bersejarah bagi perkembangan teknologi nuklir di Indonesia dan ditetapkan sebagai hari jadi BATAN.</p>
				<p class="area_text">Pada perkembangan berikutnya, untuk lebih meningkatkan penguasaan di bidang iptek nuklir, pada tahun 1965 diresmikan pengoperasian reaktor atom pertama (Triga Mark II) di Bandung. Kemudian berturut-turut, dibangun pula beberapa fasilitas litbangyasa yang tersebar di berbagai pusat penelitian, antara lain Pusat Penelitian Tenaga Atom Pasar Jumat, Jakarta (1966), Pusat Penelitian Tenaga Atom GAMA, Yogyakarta (1967), dan Reaktor Serba Guna 30 MW (1987) disertai fasilitas penunjangnya, seperti: fabrikasi dan penelitian bahan bakar, uji keselamatan reaktor, pengelolaan limbah radioaktifdanfasilitas nuklir lainnya.</p>
				<p class="area_text">Sementara itu dengan perubahan paradigma pada tahun 1997 ditetapkan UU No. 10 tentang ketenaganukliran yang diantaranya mengatur pemisahan unsur pelaksana kegiatan pemanfaatan tenaga nuklir(BATAN)dengan unsur pengawas tenaga nuklir (BAPETEN).</p></br>
